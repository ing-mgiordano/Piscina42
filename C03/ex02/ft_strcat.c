char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

#include <unistd.h>

int main(void)
{   
	char dest[] = "Hola";
    char str[] = "Mundo";

    ft_strcat(dest, str);

    write(1, &dest, 10);

}