char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0' && j < nb)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

#include <unistd.h>
#include <stdio.h>

int main(void)
{   
    char str[] = "Mundo";
    char dest[] = "Hola";

    

   printf("%s", ft_strncat(dest, str, 4));

}